                        <h3>Job Description</h3>
                           <h4>Purpose of the position</h4>
                           <p>As a Test Analyst within All In All Solutions Ltd you will be part of a small to medium sized team working on a variety of different projects. You will be reporting directly to Test Lead and will be based either at the client site or within the All In All Solutions’ office based in Swindon. The role will offer you an exciting opportunity to work within a young and expanding company with prospects of working on a variety of difference projects.</p>
                           <h4>Key responsibilities & duties</h4>

                           <ul>
                           <li>Assist and support the test lead in creation of Test estimates</li>

<li>Assist and support the test lead in the creation of a Test schedule/timeline (covering test planning, preparation and execution) that is realistic and effective</li>

<li>Be involved in all activities of testing including test planning, preparation and execution</li>

<li>Create, review and approve all Test requirements ensuring that they meet the relevant corporate standards</li>

<li>Prepare and review the Test cases ensuring that they meet the relevant corporate standards, and demonstrate the coverage as specified in the detailed test plan.</li>

<li>Support the business users through the Test execution phase providing guidance and support in all areas of execution</li>


<li>Report and manage defects to resolution by liaising with the technical teams and business users via a regular defect meeting</li>

<li>Identify and manage key risks and Issues within the test phases, keeping the Test lead fully informed</li>
<li>Ensure test environments and test data are fully sourced and available prior to Test execution</li>
<li>Report progress against plan on all testing activities keeping the Test lead informed of any slippage and providing recommendations for mitigation</li>
<li>Ensure all work is carried out and documented in accordance with required standards and procedures</li>
<li>Liaise with technical teams to ensure the environment is at the appropriate code level prior to commencing test execution</li>
<li> Participate in peer reviews of project deliverables with the business users as required, including the review of detailed requirements and design documentation</li>
<li>Assist the Test lead in preparing Test reports including the final test outcome report ensuring sign off from all stakeholders is gained.</li>



</ul>



  <h4>Essential Skills</h4>
<ul>
<li>Demonstrate strong communication skills with the ability to communicate effectively at all levels and gain the confidence of the business community by providing regular, appropriate and clear communication</li>

<li>Possess strong organisation skills demonstrating the ability to plan activities effectively and communicate them in a non-technical language</li>

<li>The ability to build strong relationships with the business users and work alongside them effectively</li>

<li>Experienced in the creation of Detailed test plans</li>

<li>Experienced in all Test activities, including planning, preparation and execution.</li>

<li>Have an awareness of all phases of testing including; System Test, System Integration, User Acceptance, non-functional, Operational Acceptance Testing</li>

<li>Production of test documentation, including test plans, test requirements, scripts, test outcome reports and metrics</li>

<li>Have an awareness of creating test estimates.</li>

<li> Extensive use of test management tools, including HP Quality Centre</li>

<li>A proven record of working in high pressure environment with changing priorities and deadlines.</li>

<li>experienced in the use of test techniques such as Risk Based Testing, Advanced Defect Management and Static Testing</li>

<li>An awareness of the full project lifecycle from Inception through to Closedown</li>

<li>Ability to identify risks and issues and recommend potential resolution and mitigation</li>
<li>Ability to identify business risk from a technical issue and communicate this effectively in a business language</li>



</ul>

